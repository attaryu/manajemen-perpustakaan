    public Map<String, String> getFormData() {
        Map<String, String> data = Map.of(
                "tanggalPinjam", DateUtils.toString(jDateChooser1.getDate()),
                "tanggalJatuhTempo", DateUtils.toString(jDateChooser2.getDate()),
                "tanggalKembali", DateUtils.toString(jDateChooser3.getDate()),
                "status", jComboBox1.getSelectedItem().toString());

        return data;
    }

