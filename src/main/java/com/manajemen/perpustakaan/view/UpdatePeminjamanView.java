    public void resetForm() {
        this.jTextField1.setText("");
        this.jTextField2.setText("");
        this.jTextField3.setText("");
        this.jTextField4.setText("");
        this.jComboBox1.setSelectedItem(-1);
        this.jDateChooser1.setDate(null);
        this.jDateChooser2.setDate(null);
        this.jDateChooser3.setDate(null);
    }

    public void setData(Map<String, String> data) {
        this.jTextField1.setText(data.get("nama"));
        this.jTextField2.setText(data.get("nrp"));
        this.jTextField3.setText(data.get("prodi"));
        this.jTextField4.setText(data.get("buku"));
        this.jDateChooser1
                .setDate(data.get("tanggalPinjam") != null ? DateUtils.toDate(data.get("tanggalPinjam"))
                        : null);
        this.jDateChooser2
                .setDate(data.get("tanggalJatuhTempo") != null
                        ? DateUtils.toDate(data.get("tanggalJatuhTempo"))
                        : null);
        this.jDateChooser3
                .setDate(data.get("tanggalKembali") != null
                        ? DateUtils.toDate(data.get("tanggalKembali"))
                        : null);

        this.jComboBox1.setSelectedItem(data.get("status"));
    }
